x = int(input("Enter a number(0 to 6) : "))

if(x == 0):
    print("Monday")
elif(x==1):
    print("Tuesday")
elif(x==2):
    print("Wednesday")
elif(x==3):
    print("Thursday")
elif(x==4):
    print("Friday")
elif(x==5):
    print("Saturday")
elif(x==6):
    print("Sunday")
else :
    print("Invalid Number Range")